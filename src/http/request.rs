use super::method::Method;
use std::fmt::{Display, Formatter, Result as FmtResult, Debug};
use std::{convert::TryFrom, error::Error};
use std::str;
pub struct Request {
    path: String,
    query_string: Option<String>,
    method: Method,
}

impl Request {
    fn from_byte_array(buf: &[u8]) -> Result<Self, String> {
        unimplemented!();
    }
}

